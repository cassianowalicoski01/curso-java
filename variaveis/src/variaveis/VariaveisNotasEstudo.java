package variaveis;

/* Aula prática sobre variáveis no Java. */
public class VariaveisNotasEstudo {
    public static void main(String[] args) {
        // Variáveis que representam a data atual.
        int dia = 13;
        int mes = 07;
        int ano = 2024;

        // Poderia inicializar as variáveis assim também, já que elas seguem o mesmo tipo int.
        // int dia = 13, mes = 07, ano = 2024;

        // Atribuição de novo valor para o dia.
        dia = 14;

        // Impresssão da data atual.
        System.out.println(dia);
        System.out.println(mes);
        System.out.println(ano);
    }
}
